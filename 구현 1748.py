
# 1자리 1~9 = 9 * 1 
# 2자리 10~99 = 90* 2 = 180 
# 3자리 100~999 = 900* 3 = 270
# 4자리 1000~9999 = 9000 * 4 = 36000

# 120이면 
# 100~120 = 21 *3 = 63
# 1800이면
# 1000~1800 = 801 *4 


n = int(input())
n_len = len(str(n))
count = 0

for i in range(n_len -1):
  count += 9*10**i * (i+1)

print(count + (n-(10**(n_len-1))+1)*n_len)
